/* 
  ASSIGNMENT OPERATORS
  --------------------

  = (asignment operator), 
  += (addition-assignment operator), 
  -= (subtraction-assignment operator), 
  *= (multiplication-assignment operator), 
  /= (division-assignment operator), 
  %= (modulus-assignment operator)

*/

let num1 = 12;
let num2 = 8;
let num3 = 4;

num1 += num2;
console.log(num1); // 20

num1 -= num3;
console.log(num1); // 16

num2 *= num3;
console.log(num2); // 32

num1 /= num3;
console.log(num1); // 4

num2 %= 7;
console.log(num2); // 4
